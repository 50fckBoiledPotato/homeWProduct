
package com.pepper.homewproduct;


class Product 
{
    public String title;
    public int price;
    
    public Product( String title, int price)
    {
        this.title = title;
        this.price = price;
    }

    public String getTitle() {
        
        return title;
    }

    public int getPrice() {
        
        return price;
    }
    
    
    
}
